//Function To Display Popup
function album_show() {
	document.getElementById('popAlbum').style.display = "block";
}

function media_show() {
	document.getElementById('popMedia').style.display = "block";
}

// Function to Hide Popup
function album_hide() {
	document.getElementById('popAlbum').style.display = "none";
}

function media_hide() {
	document.getElementById('popMedia').style.display = "none";
}